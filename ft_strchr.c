#include "libft.h"

char	*ft_strchr(char *str, char ch)
{
	if (ch == '\0')
		return (str + ft_strlen(str));
	while (*str != '\0')
	{
		if (*str == ch)
			return (str);
		str++;
	}
	return (0);
}
