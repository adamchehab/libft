#include "libft.h"

char	*ft_strrchr(char *str, int a)
{
	char *b;

	b = 0;
	while (*str != '\0')
	{
		if (a == *str)
			b = str;
		str++;
	}
	if (a == '\0')
		b = str;
	return (b);
}
